let color = "green"

switch (color) {
  case "red":
    console.log("Red is the color of passion!");
    break;
  case "blue":
    console.log("Blue is so calm and cool!");
    break;
  case "green":
    console.log("Green means you love nature!");
    break;
  case "yellow":
    console.log("Yellow is bright and cheerful!");
    break;
  case "black":
    console.log("Black is elegant and mysterious!");
    break;
  default:
    console.log("That's a nice color!");
}